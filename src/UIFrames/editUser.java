    private void editUserBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editUserBtnActionPerformed
        if (!ReNameTF.getText().equals("") && !ReUserNameTF.getText().equals("")
                && !RePassTF.getText().equals("") && !ReConfPassTF.getText().equals("") && !ReJobTF.equals("")) {
            try {
                if (!validateR()) {
                    JOptionPane.showMessageDialog(this, "Please check your wrong data", "Error", 0);
                    return;
                }
                clearErrorMsg();
                pst = dbc.conn.prepareStatement("update User_Information set user_name=?, name=?, password=?,"
                        + "jobtitle=?, phone=? where id=" + userId);
                pst.setString(2, ReNameTF.getText());
                pst.setString(1, ReUserNameTF.getText());
                pst.setString(3, RePassTF.getText());
                pst.setString(4, "emp");
                pst.setString(5, RePhoneTF.getText());
                int Flag = pst.executeUpdate();
                if (Flag == 0) {
                    JOptionPane.showMessageDialog(null, "Editing data has failed\nplease try again\nor contact Codotronic", "Error", 0);

                } else {
                    JOptionPane.showMessageDialog(null, "Editing data has been successful", "Successful", 1);
                    goBackToUsers();
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "User name already exist\nplease try another one", "Warning", 2);
                nameErrorLabel.setText("already exist");
            }
        } else {
            JOptionPane.showMessageDialog(this, "please fill the empty fields first\n(you may not enter a phone number)", "Warning", 2);
        }
    }//GEN-LAST:event_editUserBtnActionPerformed

