    private boolean validateR() {
        boolean check = true;
        if (ReNameTF.getText().length() > 200) {
            nameErrorLabel.setText("only 200 character");
            check = false;
        }
        if (ReUserNameTF.getText().length() > 45) {
            userNameErrorLabel.setText("only 45 character");
            check = false;
        }
        if (new String(RePassTF.getPassword()).length() > 45) {
            passwordErrorLabel.setText("only 45 character");
            check = false;
        }
        if (!new String(ReConfPassTF.getPassword()).equals(RePassTF.getText())) {
            conPasswordErrorLabel.setText("not the same");
            check = false;
        }
        if (RePhoneTF.getText().length() > 20) {
            phoneErrorLabel.setText("only 20 number");
            check = false;
        }
        return check;
    }

